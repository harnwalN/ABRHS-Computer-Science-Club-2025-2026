package Week_1;

public class DataTypes {
    public static void main (String[] args) {
        // Important Data Types
        // Variables are intialized with a data type and value
        int myInt = 6;
        double myDouble = 6.7;
        boolean myBool = true; 
        char myChar = 'a'; 
        String myString = "Chris is the goat!";
        
        // Variables may also be declared and assigned separately
        int anotherInt; // declaration
        anotherInt = 7; // assignment


        System.out.println("Integer: " + myInt);
        System.out.println("Double: " + myDouble);
        System.out.println("Boolean: " + myBool);
        System.out.println("Character: " + myChar);
        System.out.println("String: " + myString);
        System.out.println("Another Integer: " + anotherInt);
    }
}
